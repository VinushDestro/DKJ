package dkjconstruction.hr;

/**
 *
 * @author ASRAJ
 */
public class HRDetails {
    
    
    private String EmpId;
    private String Name;
    private String Address;
    private String Nic;
    private String Dob;
    private String Gender;
    private String ContactNo;
    private String Position;
    private String EmpType;
    private String BasicSalary;
    private String DaySalary;

    public HRDetails(String EmpId, String Name, String Address, String Nic, String Dob, String Gender, String ContactNo, String Position, String EmpType, String BasicSalary, String DaySalary) {
        this.EmpId = EmpId;
        this.Name = Name;
        this.Address = Address;
        this.Nic = Nic;
        this.Dob = Dob;
        this.Gender = Gender;
        this.ContactNo = ContactNo;
        this.Position = Position;
        this.EmpType = EmpType;
        this.BasicSalary = BasicSalary;
        this.DaySalary = DaySalary;
    }

    /**
     * @return the EmpId
     */
    public String getEmpId() {
        return EmpId;
    }

    /**
     * @param EmpId the EmpId to set
     */
    public void setEmpId(String EmpId) {
        this.EmpId = EmpId;
    }

    /**
     * @return the Name
     */
    public String getName() {
        return Name;
    }

    /**
     * @param Name the Name to set
     */
    public void setName(String Name) {
        this.Name = Name;
    }

    /**
     * @return the Address
     */
    public String getAddress() {
        return Address;
    }

    /**
     * @param Address the Address to set
     */
    public void setAddress(String Address) {
        this.Address = Address;
    }

    /**
     * @return the Nic
     */
    public String getNic() {
        return Nic;
    }

    /**
     * @param Nic the Nic to set
     */
    public void setNic(String Nic) {
        this.Nic = Nic;
    }

    /**
     * @return the Dob
     */
    public String getDob() {
        return Dob;
    }

    /**
     * @param Dob the Dob to set
     */
    public void setDob(String Dob) {
        this.Dob = Dob;
    }

    /**
     * @return the Gender
     */
    public String getGender() {
        return Gender;
    }

    /**
     * @param Gender the Gender to set
     */
    public void setGender(String Gender) {
        this.Gender = Gender;
    }

    /**
     * @return the ContactNo
     */
    public String getContactNo() {
        return ContactNo;
    }

    /**
     * @param ContactNo the ContactNo to set
     */
    public void setContactNo(String ContactNo) {
        this.ContactNo = ContactNo;
    }

    /**
     * @return the Position
     */
    public String getPosition() {
        return Position;
    }

    /**
     * @param Position the Position to set
     */
    public void setPosition(String Position) {
        this.Position = Position;
    }

    /**
     * @return the EmpType
     */
    public String getEmpType() {
        return EmpType;
    }

    /**
     * @param EmpType the EmpType to set
     */
    public void setEmpType(String EmpType) {
        this.EmpType = EmpType;
    }

    /**
     * @return the BasicSalary
     */
    public String getBasicSalary() {
        return BasicSalary;
    }

    /**
     * @param BasicSalary the BasicSalary to set
     */
    public void setBasicSalary(String BasicSalary) {
        this.BasicSalary = BasicSalary;
    }

    /**
     * @return the DaySalary
     */
    public String getDaySalary() {
        return DaySalary;
    }

    /**
     * @param DaySalary the DaySalary to set
     */
    public void setDaySalary(String DaySalary) {
        this.DaySalary = DaySalary;
    }
    

   

    
   
    
}
