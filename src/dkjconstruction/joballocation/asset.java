package dkjconstruction.joballocation;

/**
 *
 * @author Ranjitha
 */
public class asset {
    
    private String reg;
    private String name;
    private String type;

    public String getReg() {
        return reg;
    }

    public void setReg(String reg) {
        this.reg = reg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public asset(String reg, String name, String type) {
        this.reg = reg;
        this.name = name;
        this.type = type;
    }
}
